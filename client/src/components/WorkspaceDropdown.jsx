import { useState, useRef, useEffect } from "react";
import { Check, Plus, ChevronsUpDown } from "lucide-react";
import { useDispatch, useSelector } from "react-redux";
import { setCurrentWorkspace } from "../features/workspaceSlice";
import { useNavigate } from "react-router-dom";
import { useClerk, useOrganizationList } from "@clerk/clerk-react";

function WorkspaceDropdown() {
  const { workspaces } = useSelector((state) => state.workspace);
  const currentWorkspace = useSelector(
    (state) => state.workspace?.currentWorkspace || null
  );
  const [isOpen, setIsOpen] = useState(false);
  const dropdownRef = useRef(null);

  const dispatch = useDispatch();
  const navigate = useNavigate();
  const { setActive, userMemberships, isLoaded } = useOrganizationList({
    userMemberships: true,
  });
  const { openCreateOrganization } = useClerk();

  const onSelectWorkspace = (organizationId) => {
    setActive({ organization: organizationId });
    dispatch(setCurrentWorkspace(organizationId));
    setIsOpen(false);
    navigate("/");
  };

  // Close dropdown on outside click
  useEffect(() => {
    function handleClickOutside(event) {
      if (dropdownRef.current && !dropdownRef.current.contains(event.target)) {
        setIsOpen(false);
      }
    }
    document.addEventListener("mousedown", handleClickOutside);
    return () => document.removeEventListener("mousedown", handleClickOutside);
  }, []);

  useEffect(() => {
    if (currentWorkspace && isLoaded) {
      setActive({ organization: currentWorkspace.id });
    }
  }, [currentWorkspace, isLoaded, setActive]);

  return (
    <div className="relative m-4" ref={dropdownRef}>
      <button
        onClick={() => setIsOpen((prev) => !prev)}
        className="w-full flex items-center justify-between p-3 h-auto text-left rounded hover:bg-gray-100 dark:hover:bg-zinc-800"
      >
        <div className="flex items-center gap-3">
          <img
            src={currentWorkspace?.image_url}
            alt={currentWorkspace?.name}
            className="w-8 h-8 rounded shadow"
          />
          <div className="min-w-0 flex-1">
            <p className="font-semibold text-gray-800 dark:text-white text-sm truncate">
              {currentWorkspace?.name || "Select Workspace"}
            </p>
            <p className="text-xs text-gray-500 dark:text-zinc-400 truncate">
              {workspaces.length} workspace{workspaces.length !== 1 ? "s" : ""}
            </p>
          </div>
        </div>
        <span className="bg-white dark:bg-zinc-800 p-1 border border-gray-200 dark:border-zinc-700 rounded-md">
          <ChevronsUpDown className="w-3 h-3 text-gray-500 dark:text-zinc-400 flex-shrink-0" />
        </span>
      </button>

      {isOpen && (
        <div className="absolute z-50 w-64 bg-white dark:bg-zinc-900 border border-gray-200 dark:border-zinc-700 rounded shadow-lg top-full left-0">
          <div className="p-2">
            <p className="text-xs text-gray-500 dark:text-zinc-400 uppercase tracking-wider mb-2 px-2">
              Workspaces
            </p>
            {userMemberships.data.map(({ organization }) => (
              <div
                key={organization?.id}
                onClick={() => onSelectWorkspace(organization?.id)}
                className="flex items-center gap-3 p-2 my-2 cursor-pointer ring-1 ring-gray-200 dark:ring-zinc-700 rounded hover:bg-gray-100 dark:hover:bg-zinc-800"
              >
                <img
                  src={organization?.imageUrl}
                  alt={organization?.name}
                  className="w-6 h-6 rounded"
                />
                <div className="flex-1 min-w-0">
                  <p className="text-sm font-medium text-gray-800 dark:text-white truncate">
                    {organization?.name}
                  </p>
                  <p className="text-xs text-gray-500 dark:text-zinc-400 truncate">
                    {organization?.membersCount || 0} members
                  </p>
                </div>
                {currentWorkspace?.id === organization?.id && (
                  <Check className="w-4 h-4 text-[#0b996f] dark:text-[#0b996f]/90 flex-shrink-0" />
                )}
              </div>
            ))}
          </div>

          <hr className="border-gray-200 dark:border-zinc-700" />

          <div
            onClick={() => {
              openCreateOrganization();
              setIsOpen(false);
            }}
            className="p-2 cursor-pointer rounded group hover:bg-gray-100 dark:hover:bg-zinc-800"
          >
            <p className="flex items-center text-xs gap-2 my-1 w-full text-[#0b996f] dark:text-[#0b996f]/90 group-hover:text-[#0b996f] dark:group-hover:text-[#0b996f]/90">
              <Plus className="w-4 h-4" /> Create Workspace
            </p>
          </div>
        </div>
      )}
    </div>
  );
}

export default WorkspaceDropdown;
